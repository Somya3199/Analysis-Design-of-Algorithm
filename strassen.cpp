#include<iostream>  
using namespace std;  
int main()  
{  
int a[2][2]={1,2,3,4};
int b[2][2]={5,6,7,8},c[2][2];
int i,j,p,q,r,s,t,u,v;    
cout<<"multiply of the matrix=\n";    
p=(a[0][0]+a[1][1])*(b[0][0]+b[1][1]);
q=(a[1][0]+a[1][1])*(b[0][0]);
r=(a[0][0])*(b[0][1]-b[1][1]);
s=(a[1][1])*(b[1][0]-b[0][0]);
t=(a[0][0]+a[0][1])*(b[1][1]);
u=(a[1][0]-a[0][0])*(b[0][0]+b[0][1]);
v=(a[0][1]-a[1][1])*(b[1][0]+b[1][1]);

c[0][0]=p+s-t+v;
c[0][1]=r+t;
c[1][0]=q+s;
c[1][1]=p-q+r+u;
cout<<c[0][0]<<"\t"<<c[0][1]<<"\n"<<c[1][0]<<"\t"<<c[1][1];    
cout<<"\n";
return 0;     
}

